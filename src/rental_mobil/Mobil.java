private void TampilData (String sql){
    DefaultTableModel t = new DefaultTableModel();
    
    t.addColumn("ID Mobil");
    t.addColumn("Nama Mobil");
    t.addColumn("Merek Mobil");
    t.addColumn("Harga Rental");
    t.addColumn("Status");
    Table.setModel(t);
    try {
        int i = 1;
        stat=con.createStatement();
        res=stat.executeQuery("select * from db_mobil");
        while (res.next()) {
            t.addRow(new Object[]{
            res.getString("id_mobil"),
            res.getString("nama_mobil"),
            res.getString("merk_mobil"),
            res.getString("harga_rental"),
            res.getString("status"),
            });
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(rootPane, e);
    }
}



