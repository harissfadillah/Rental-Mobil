    private void id_mobilKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_id_mobilKeyPressed
        // TODO add your handling code here:
        id=id_mobil.getText(); 
        int tekanenter=evt.getKeyCode();
        if (tekanenter==10){
            try{
                sql="select * from db_mobil "
                        + "where id_mobil='"+ id + "'";
                stat=con.createStatement();
                res=stat.executeQuery(sql);
                while (res.next()){
                    nama_mobil.setText(res.getString("nama_mobil"));
                    merk_mobil.setText(res.getString("merk_mobil"));
                    harga_rental.setText(res.getString("harga_rental"));
                    statuscombobox.setSelectedItem(res.getString("status"));
                    JOptionPane.showMessageDialog(null, "Data Ditemukan");
                }
            }catch (Exception e){
                JOptionPane.showMessageDialog(null, "Tidak Ditemukan \n"+e.getMessage());
                nama_mobil.requestFocus();
            }
        }
    }//GEN-LAST:event_id_mobilKeyPressed

