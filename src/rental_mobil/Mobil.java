    private void SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanActionPerformed
        // TODO add your handling code here:
        id=String.valueOf(id_mobil.getText());
        mobil=String.valueOf(nama_mobil.getText());
        merk=String.valueOf(merk_mobil.getText());
        harga=Integer.parseInt(harga_rental.getText());
        status=String.valueOf(statuscombobox.getSelectedItem());
        try{
            sql="INSERT INTO db_mobil(id_mobil, nama_mobil, "
                    + "merk_mobil, harga_rental, "
                    + "status)value "
                    + "('"+ id +"','"+ mobil +"','"+ merk +"',"
                    + "'"+ harga +"','"+ status +"')";
            stat=con.createStatement();
            stat.execute(sql);
            kosongkan();
            TampilData(sql);
            JOptionPane.showMessageDialog(null,
                    "Data Berhasil Disimpan");
        }catch (Exception e){
            JOptionPane.showMessageDialog(null,
                    "Data Gagal Disimpan\n"+e.getMessage());
        }
    }//GEN-LAST:event_SimpanActionPerformed

